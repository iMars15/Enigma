/* ─── Toast ──────────────────────────────── */
const Toast = (() => {
  let container;

  function init() {
    container = document.getElementById('toast-container');
    if (!container) {
      container = document.createElement('div');
      container.id = 'toast-container';
      document.body.appendChild(container);
    }
  }

  function show(message, type = 'info', duration = 3500) {
    if (!container) init();

    const el = document.createElement('div');
    el.className = `toast ${type}`;
    el.textContent = message;
    container.appendChild(el);

    setTimeout(() => {
      el.style.opacity = '0';
      el.style.transform = 'translateX(20px)';
      el.style.transition = 'all 0.25s ease';
      setTimeout(() => el.remove(), 260);
    }, duration);
  }

  return {
    success: (msg, dur) => show(msg, 'success', dur),
    error:   (msg, dur) => show(msg, 'error', dur),
    info:    (msg, dur) => show(msg, 'info', dur),
  };
})();

/* ─── Modal ──────────────────────────────── */
const Modal = (() => {
  function open(overlayEl) {
    overlayEl.classList.add('active');
    document.body.style.overflow = 'hidden';

    // Close on overlay click
    overlayEl.addEventListener('click', (e) => {
      if (e.target === overlayEl) close(overlayEl);
    }, { once: true });

    // Close on Escape
    const onKey = (e) => {
      if (e.key === 'Escape') { close(overlayEl); document.removeEventListener('keydown', onKey); }
    };
    document.addEventListener('keydown', onKey);
  }

  function close(overlayEl) {
    overlayEl.classList.remove('active');
    document.body.style.overflow = '';
  }

  function create({ title, content, actions = [] }) {
    const overlay = document.createElement('div');
    overlay.className = 'modal-overlay';
    overlay.innerHTML = `
      <div class="modal">
        <div class="modal-header">
          <span class="modal-title">${title}</span>
          <button class="btn-icon modal-close-btn">
            ${Icons.x()}
          </button>
        </div>
        <div class="modal-body">${content}</div>
        ${actions.length ? `<div class="modal-footer" style="display:flex;gap:8px;justify-content:flex-end;margin-top:20px">
          ${actions.map(a => `<button class="btn ${a.class || 'btn-ghost'}" data-action="${a.key}">${a.label}</button>`).join('')}
        </div>` : ''}
      </div>
    `;

    document.body.appendChild(overlay);

    overlay.querySelector('.modal-close-btn')?.addEventListener('click', () => {
      close(overlay);
      setTimeout(() => overlay.remove(), 400);
    });

    overlay.addEventListener('click', (e) => {
      if (e.target === overlay) {
        close(overlay);
        setTimeout(() => overlay.remove(), 400);
      }
    });

    setTimeout(() => open(overlay), 10);

    return {
      el: overlay,
      close: () => { close(overlay); setTimeout(() => overlay.remove(), 400); },
      on: (key, fn) => {
        overlay.querySelector(`[data-action="${key}"]`)?.addEventListener('click', fn);
      },
    };
  }

  return { open, close, create };
})();

/* ─── Confirm dialog ─────────────────────── */
function confirm(message, opts = {}) {
  return new Promise((resolve) => {
    const m = Modal.create({
      title: opts.title || 'Подтверждение',
      content: `<p style="color:var(--text2);font-size:.9rem;line-height:1.6">${message}</p>`,
      actions: [
        { key: 'cancel', label: opts.cancel || 'Отмена', class: 'btn-ghost' },
        { key: 'ok',     label: opts.ok     || 'OK',     class: opts.danger ? 'btn-danger' : 'btn-primary' },
      ],
    });
    m.on('cancel', () => { m.close(); resolve(false); });
    m.on('ok',     () => { m.close(); resolve(true);  });
  });
}

