/* ─── Avatar placeholder ─────────────────── */
function avatarPlaceholder(name, size = 'md') {
  const letter = (name || '?')[0].toUpperCase();
  const colors = ['#00d28c','#00a86b','#1e90ff','#ffa502','#ff6b6b'];
  const color = colors[letter.charCodeAt(0) % colors.length];
  const sizes = { xs:28, sm:36, md:44, lg:60, xl:96, xxl:120 };
  const px = sizes[size] || 44;
  const fontSize = Math.round(px * 0.38);

  const el = document.createElement('div');
  el.className = `avatar-placeholder avatar-${size}`;
  el.style.cssText = `width:${px}px;height:${px}px;font-size:${fontSize}px;background:${color}18;color:${color};border-color:${color}40`;
  el.textContent = letter;
  return el;
}

/* ─── Time formatting ────────────────────── */
function formatTime(dateStr) {
  const d = new Date(dateStr);
  return d.toLocaleTimeString('ru-RU', { hour: '2-digit', minute: '2-digit' });
}

function formatDate(dateStr) {
  const d = new Date(dateStr);
  const now = new Date();
  const diff = (now - d) / 1000;

  if (diff < 86400 && d.getDate() === now.getDate()) return formatTime(dateStr);
  if (diff < 172800) return 'Вчера';
  return d.toLocaleDateString('ru-RU', { day: 'numeric', month: 'short' });
}

function formatDuration(sec) {
  const m = Math.floor(sec / 60).toString().padStart(2,'0');
  const s = (sec % 60).toString().padStart(2,'0');
  return `${m}:${s}`;
}

/* ─── Debounce ───────────────────────────── */
function debounce(fn, delay = 300) {
  let t;
  return (...args) => { clearTimeout(t); t = setTimeout(() => fn(...args), delay); };
}

/* ─── Auto-resize textarea ───────────────── */
function autoResize(textarea) {
  textarea.style.height = 'auto';
  textarea.style.height = Math.min(textarea.scrollHeight, 140) + 'px';
}

/* ─── Redirect guard ─────────────────────── */
function requireAuth() {
  if (!Api.auth.isLoggedIn()) {
    window.location.href = '/frontend/pages/auth.html';
    return false;
  }
  return true;
}

function redirectIfAuth() {
  if (Api.auth.isLoggedIn()) {
    window.location.href = '/frontend/pages/app.html';
    return true;
  }
  return false;
}

